# If Condition Program: Follow Indentation while coding

a, b = 1, "India"
if a != 1 and b != "India":
    print("True")
else:
    print(b)

# For Loop: Follow Indentation while coding

# Enhanced For Loop
e = [1, 2, 3, 4, 5, "Friend"]
for d in e:
    print(d*2)

# Normal For Loop: To print sum of natural number
value = 0
for i in range(1, 10):
    value = value + i
print(value)

# To print value by adding values by 2 (i++):

for k in range(1, 8, 2):
    print(k)

# To print range from zero to Five
for j in range(5):
    print(j)

# While Loop: Follow Code Indentation

h = 10
while h>=10:
    print("I'm Greater than given value")
    break
# While Condition using IF Block
it = 4
while it > 1:
    if it != 3:
        print(it)
    it = it - 1
print("While Loop Completed")

# Using Continue Keyword
it = 10
while it>1:
    if it == 9:
        it = it - 1
        continue
    if it == 3:
        break
    print(it)
    it = it - 1




